const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  pic: {
    type: String,
    default: 'https://www.poutstation.com/upload/photos/avatar.jpg',
  },
});

mongoose.model('User', userSchema);
